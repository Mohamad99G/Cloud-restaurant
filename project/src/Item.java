
class Item {
    int id;
    String name;
    int preparingTime;
    float cost;
    Integer count;

    public Item(int id, String name, int preparingTime, float cost, Integer count) {
        this.id = id;
        this.name = name;
        this.preparingTime = preparingTime;
        this.cost = cost;
        this.count = count;
    }

}

